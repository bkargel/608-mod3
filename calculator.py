# Using the math module to calculate square root of x
# x = 900

import math

square_root = math.sqrt(900)

# Using the math module to round x to the largest integer not greater than x
# x = 9.2

floor = math.floor(9.2)

print('The square root of 900 is', square_root)
print('The floor of 9.2 is', floor)
