#Calculate the population variance and population standard deviation for the following data set: 1, 3, 4, 2, 6, 5, 3, 4, 5, 2

import statistics

statistics.pvariance([1, 3, 4, 2, 6, 5, 3, 4, 5, 2])   # population variance

statistics.pstdev([1, 3, 4, 2, 6, 5, 3, 4, 5, 2])    # poopulation standard deviation